import { chunk, uniq, flatten } from "fake-lodash";
console.log("=== node_modules Resolution Test ===\n");
const arr = [
	1,
	2,
	3,
	4,
	5,
	6,
	7,
	8
];
console.log(`Original array: ${JSON.stringify(arr)}`);
console.log(`chunk(arr, 3): ${JSON.stringify(chunk(arr, 3))}`);
const duplicates = [
	1,
	2,
	2,
	3,
	3,
	3,
	4
];
console.log(`\nArray with duplicates: ${JSON.stringify(duplicates)}`);
console.log(`uniq(duplicates): ${JSON.stringify(uniq(duplicates))}`);
const nested = [
	[1, 2],
	[3, 4],
	[5]
];
console.log(`\nNested array: ${JSON.stringify(nested)}`);
console.log(`flatten(nested): ${JSON.stringify(flatten(nested))}`);
